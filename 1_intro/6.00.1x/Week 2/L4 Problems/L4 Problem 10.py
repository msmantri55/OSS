# Define a function isVowel(char) that returns True if char is a vowel ('a',
# 'e', 'i', 'o', or 'u'), and False otherwise. You can assume that char is a
# single letter of any case (ie, 'A' and 'a' are both valid).

def isVowel(char):
    return char == 'a' or char == 'A' or char == 'e' or char == 'E' or char == 'I' or char == 'i' or char == 'O' or char == 'o' or char == 'U' or char == 'u'